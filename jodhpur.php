<h2>	04 UMAID BHAWAN PALACE<br></h2>

<div class="container">
	<img class="slides" src="ubp.webp" height="350px" width="100%">
</div><br><br>

<div class="content">
The Umaid Bhawan Palace, originally called the Chittar Palace, is one of the world’s largest private residences. While a part of the palace is still used by the Jodhpur Royal Family, another part has been leased out to the Taj Group of Hotels and it is now being run as a heritage hotel by them.The palace has about 347 rooms and is divided into three parts.
<br><br>
One part of the palace is the royal residence, the other part is a heritage hotel and the third part is a museum. The Umaid Bhawan Palace museum showcases the 20th century history of the Jodhpur Royal Family. The museum also has a gallery which showcases the automobiles owned by the royal family.The Umaid Bhawan Palace was initially called the Chittar Palace due to its location on the Chittar Hill, which is the highest point in Jodhpur. The construction of the palace was done in 1929 to provide employment to thousands during the famine..
