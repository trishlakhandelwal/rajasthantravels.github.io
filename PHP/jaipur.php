	<h2>	08 JANTAR MANTAR OBSERVATORY<br></h2>

<div class="container">
	<img class="slides" src="Jantarmantar.jpg" height="350px" width="100%">
</div><br><br>

<div class="content">
The Jantar Mantar Observatory in Jaipur is a collection of architectural astronomical instruments. The Jantar Mantar was built by Maharaja Sawai Jai Singh II who served as a commander to Mughal Emperor Aurangzeb. The Jantar Mantar in Jaipur is architecturally based on the one that is present in New Delhi. Both of them were built by Maharaja Sawai Jai Singh II. The king constructed five such structures in different places including Delhi, Ujjain and Mathura. The Jantar Mantar in Jaipur is the best preserved among all these structures and has been included in the list of World Heritage Sites by UNESCO.<br><br>

The Jantar Mantar attracts a number of astronomers, students of Vedic astronomy and tourists from all over the world. Local astronomers still use it to predict the weather for local farmers. The Jantar Mantar can be called the single most representative work of Vedic astronomy.<br><br>

The Jantar Mantar houses a giant sun dial called the Samrat Yantra and other astronomical instruments such as the Nadivalya Yantra, the Jai Prakash Yantra and the Laghu Samrat Yantra.<br>
