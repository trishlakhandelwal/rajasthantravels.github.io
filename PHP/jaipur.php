<h2>	04 HAWA MAHAL<br></h2>

<div class="container">
	<img class="slides" src="Hawa_Mahal.jpg" height="350px" width="100%">
</div><br><br>

<div class="content">
Hawa Mahal (English translation: "Palace of Winds" or "Palace of the Breeze") is a palace in
Jaipur, India. It is constructed of red and pink sandstone. The palace sits on the edge of the City
Palace, Jaipur, and extends to the zenana, or women's chambers.
<br><br>
<b>Distance or location:</b> The address of Hawa Mahal is Hawa Mahal Road in Badi Chopad located in J.D.A. Market, Kanwar Nagar, Jaipur, Rajasthan.<br><br>
<b>Best time to visit:</b> The best time to visit Hawa Mahal is in between the months of October to March because the weather during this time span is considered to be conducive enough to traverse the place in totality. The temperature at this place ranges in between 22 Degree Celsius to 5 Degree Celsius making the weather salubrious and pleasant to traverse and feel happy.<br> <br>
<b>Charges:</b> Charges incurred on visiting Hawa Mahal is 5 INR or 0.077 USD for both Indians and Foreigners.<br>

<br></div>

<div class="content" id="2" style="display:none;">
The structure was built in 1799 by Maharaja Sawai Pratap Singh. He was so inspired by the unique structure of Khetri
Mahal that he built this grand and historical palace. It was designed by Lal Chand Ustad. Itsunique five-story exterior is akin to the honeycomb of a beehive with its 953 small windows
called jharokhas decorated with intricate latticework.
The original intent of the lattice design
was to allow royal ladies to observe everyday life and festivals celebrated in the street below
without being seen, since they had to obey the strict rules of "purdah", which forbade them from
appearing in public without face coverings. This architectural feature also allowed cool air from
the Venturi effect to pass through, thus making the whole area more pleasant during the high
temperatures in summer. Many people see the Hawa Mahal from the street view and think it is
the front of the palace, but in reality it is the back of that structure.
