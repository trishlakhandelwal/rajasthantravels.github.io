<br></div>

<div class="content" id="4" style="display:none;">
The popular belief is that Nahar herestands for Nahar Singh Bhomia, whose spirit haunted the place and obstructed construction of the fort. Nahar's spirit was pacified by building a temple in his memory within the fort, which thus became known by his name. Built mainly in 1734 by Maharaja Sawai Jai Singh, the king of Jaipur, the fort was constructed as a place of retreat on the summit of the ridge above the city.Walls extended over the surrounding hills, forming fortifications that connected this fort toJaigarh, the fort above the old capital of Amber. Though the fort never came under attack during the course of its history, it did see some historical events, notably the treaties with the Maratha forces who warred with Jaipur in the 18th century. During the Indian Mutiny of 1857, the Europeans of the region, including the British Resident's wife, were moved to Nahargarh fort bythe king of Jaipur, Sawai Ram Singh, for their protection.
